import mongoose from 'mongoose'

const teamSchema = new mongoose.Schema({

    name:{
        type: String,
        default: 'team'
    },
    email: {
        type: String
    },
    schedule: {
        type : Number,

    },
    numberPlayers: {
        type: Number,
        
    }
   





},{ timestamps:true})

let Dataset = mongoose.model('team', teamSchema)
export default Dataset;